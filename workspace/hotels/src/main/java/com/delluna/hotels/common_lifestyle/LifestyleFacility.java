package com.delluna.hotels.common_lifestyle;

public class LifestyleFacility {
	private int lifeFacility_no;
	private String lifeFacility_image_name;
	private long lifeFacility_image_size;
	private String lifeFacility_title;
	private String lifeFacility_info;
	private Lifestyle lifestyle;
	
	public int getLifeFacility_no() {
		return lifeFacility_no;
	}
	public void setLifeFacility_no(int lifeFacility_no) {
		this.lifeFacility_no = lifeFacility_no;
	}
	public String getLifeFacility_image_name() {
		return lifeFacility_image_name;
	}
	public void setLifeFacility_image_name(String lifeFacility_image_name) {
		this.lifeFacility_image_name = lifeFacility_image_name;
	}
	public long getLifeFacility_image_size() {
		return lifeFacility_image_size;
	}
	public void setLifeFacility_image_size(long lifeFacility_image_size) {
		this.lifeFacility_image_size = lifeFacility_image_size;
	}
	public String getLifeFacility_title() {
		return lifeFacility_title;
	}
	public void setLifeFacility_title(String lifeFacility_title) {
		this.lifeFacility_title = lifeFacility_title;
	}
	public String getLifeFacility_info() {
		return lifeFacility_info;
	}
	public void setLifeFacility_info(String lifeFacility_info) {
		this.lifeFacility_info = lifeFacility_info;
	}
	public Lifestyle getLifestyle() {
		return lifestyle;
	}
	public void setLifestyle(Lifestyle lifestyle) {
		this.lifestyle = lifestyle;
	}
	
}
